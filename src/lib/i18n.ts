export function t(
  locale: Locale,
  key: string,
  params?: Record<string, string | number>
): string {
  let text = messages[locale]?.[key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}
